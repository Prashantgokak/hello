def add(a, b):
    return a+b

def sub(a,b):
    return a-b

def mul(a,b):
    return a*b

def div(a,b):
    return a/b


if __name__=="__main__":
    x=10
    y=20
    print("addition:",add(x,y))
    print("subtraction:",sub(x,y))
    print("multiplication:",mul(x,y))       
    print("division:",div(x,y))
